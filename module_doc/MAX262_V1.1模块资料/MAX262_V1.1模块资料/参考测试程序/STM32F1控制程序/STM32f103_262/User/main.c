// 头文件包含 
//-----------------------------------------------------------------
#include <stm32f10x.h>
#include "delay.h"
#include "PeripheralInit.h"
#include "MAX262.h"


//-----------------------------------------------------------------
// 主程序
//-----------------------------------------------------------------
int main(void)
{
	MAX262_GPIO_Init();                   // IO口初始化配置
	Filter1(0,1,0.842);                    // (mode)控制方式mode, 中心频率f, 品质因数q 
	Filter2(0,1,0.842);                    // (mode)控制方式mode, 中心频率f, 品质因数q 
	Delay_1ms(100);		   
	GPIOB->ODR =0;
	while(1);	   
}

