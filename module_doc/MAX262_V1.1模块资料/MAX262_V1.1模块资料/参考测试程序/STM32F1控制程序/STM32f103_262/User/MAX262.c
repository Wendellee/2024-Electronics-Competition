#include <stm32f10x.h>
#include "MAX262.h"
#include "delay.h"
#include <math.h>
#define fclk1   0.1398    //139.8k时钟频率
#define fclk2   0.1398    //139.8k时钟频率

//-----------------------------------------------------------------
// 功能程序区
//-----------------------------------------------------------------

//-----------------------------------------------------------------
// u8 Fn(float f)                     
//-----------------------------------------------------------------
// 函数功能: 计算f的频率控制字N
// 入口参数: 截止频率(中心频率) f
// 返 回 值: 控制字N
// 全局变量: 无
// 注意事项: 无
//-----------------------------------------------------------------
u8 Fn1(float f)                      //频率关键字
{
    u8 temp;
    temp = (fclk1*637)-26;         //原本是*2/pi;即*0.637  fclk用M单位，f单位khz
    return temp;  	                //具体上芯片手册上的公式
}

u8 Fn2(float f)                      //频率关键字
{
    u8 temp;
    temp = (fclk2*637)-26;         //原本是*2/pi;即*0.637  fclk用M单位，f单位khz
    return temp;  	                //具体上芯片手册上的公式
}
//-----------------------------------------------------------------
// u8 Qn(float q)                     
//-----------------------------------------------------------------
// 函数功能: 计算Q的控制字N
// 入口参数: Q
// 返 回 值: 控制字N
// 全局变量: 无
// 注意事项: 无
//-----------------------------------------------------------------
u8 Qn(float q)                     //品质因数关键字
{
    u8 temp;
    temp = 128-(64/q);             //具体看芯片手册上有公式
    return temp; 
}
//-----------------------------------------------------------------
// void Filter1(uint8_t mode, float q)
//-----------------------------------------------------------------
// 函数功能: 滤波器的模式、频率以及Q值的设置
// 入口参数: 模式 mode, 截止/中心频率 f, Q值 q
// 返 回 值: 无
// 全局变量: 无
// 注意事项: 无
//-----------------------------------------------------------------
void Filter1(uint8_t mode, float f, float q)
{
	uint8_t i;
	uint8_t a = 0x03;
	uint8_t sf, sq;
	i  = sf = sq = 0;
	sf = Fn1(f);                     // 求出其频率控制字N
	sq = Qn(q);                     // 求出Q对应的控制字N
	LE_H;                           // 使能端拉高
	Delay_ns(200);
	WR_H;                           // 写端口拉高
	Delay_ns(200);
	
	// 具体可见芯片手册 P15 Table4	
	
	GPIOB->BRR = 0x3f00;	          // 写入模式的地址
	Delay_ns(200);
	WR_L;                           //写端口拉低
	Delay_ns(200);
// 	GPIOB->BRR = 0x0300;
	GPIOB->ODR |= ((uint16_t)(mode & 0x03) << 8);    // 将模式控制字送给D1, D0
	Delay_ns(200);
	WR_H;                           // 写端口拉高
	Delay_ns(200);
	
	for(i = 0; i < 3; i++)
	{
		GPIOB -> BRR = 0x3f00;	          // 现将地址与数据位清0
		GPIOB -> ODR |= (uint16_t)(i+1)<<10;          // 写入地址
		Delay_ns(200);
		WR_L;                             // 写入拉低
		Delay_ns(200);
// 		GPIOB->BRR = 0x0300;            //数据位清0
		GPIOB->ODR |= ((uint16_t)(sf & a) << (8-2*i));		  // 将f的控制字N写入
		Delay_ns(200);                   
		WR_H;                             // 写入拉高
		a = a << 2;                       // a左移2位		
	}
	
	a = 0x03;
	
	for(i = 0; i < 4; i++)
	{
		GPIOB -> BRR = 0x3f00;	          // 现将地址与数据位清0
		GPIOB -> ODR |= (uint16_t)(i+4)<<10;          // 写入地址
		Delay_ns(200);
		WR_L;                             // 写入拉低
		Delay_ns(200);
// 		GPIOB->BRR = 0x0300;              //数据位清0
		GPIOB->ODR |= ((uint16_t)(sq & a) << (8-2*i));		  // 将Q的控制字N写入
		Delay_ns(200);                   
		WR_H;                             // 写入拉高
		a = a << 2;                       // a左移2位		
	}
}

